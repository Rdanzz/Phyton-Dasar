# Operasi Komparasi

# Setiap hasil dari operasi komparasi adalah boolean true atau false

#  >, < , >=, <=, ==, !=

a = 4
b = 5

# > lebih dari
print(f"{a} > {b} = {a > b}")

# < kurang dari
print(f"{a} < {b} = {a < b}")

# >= lebih dari sama dengan
print(f"{a} >= {b} = {a >= b}")

# <= kurang dari sama dengan
print(f"{a} <= {b} = {a <= b}")

# == sama dengan
print(f"{a} == {b} = {a == b}")

# != tidak sama dengan
print(f"{a} != {b} = {a != b}")




