# Operator Bitwise, Operasi Biner, Binary


a = 9
b = 5

# bitwise (|) or
c = a | b 
print("==== OPERATOR ====")
print(f"nilai {a} binary {format(a, '08b')}")
print(f"nilai {b} binary {format(b, '08b')}")
print("================================ (|)")
print(f"nilai {c} binary {format(c, '08b')}")


# bitwise (&) and
c = a & b 
print("\n==== OPERATOR ====")
print(f"nilai {a} binary {format(a, '08b')}")
print(f"nilai {b} binary {format(b, '08b')}")
print("================================ (&)")
print(f"nilai {c} binary {format(c, '08b')}")


# bitwise (^) xor
c = a ^ b 
print("\n==== OPERATOR ====")
print(f"nilai {a} binary {format(a, '08b')}")
print(f"nilai {b} binary {format(b, '08b')}")
print("================================ (^)")
print(f"nilai {c} binary {format(c, '08b')}")



