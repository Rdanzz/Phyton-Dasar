# Operator dalam bentuk Method

# merubah case dari string

# merubah ke uppercase
salam = "hallo, selamat pagi"
print(salam.upper())

# merubah ke lowercase
salam = "Hallo, Selamat pagi"
print(salam.lower())

# pengecekkan dengan isX method
# example lowercase method
salam = "hallo, selamat pagi"
print(f"{salam} adalah lower semua : {salam.islower()}")

# example uppercase method
print(f"{salam} adalah upper semua : {salam.isupper()}")
