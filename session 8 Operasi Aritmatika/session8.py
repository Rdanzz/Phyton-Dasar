#  Operasi aritmatika

a = 10
b = 3

# penambahan
hasil = a + b
# ribet
print(a, "+", b, "=", hasil)

# pengurangan
hasil = a - b
print(f"{a} - {b} = {hasil}")

# perkalian
hasil = a * b
print(f"{a} * {b} = {hasil}")

# pembagian
hasil = a / b
print(f"{a} / {b} = {hasil}")

# floor division
hasil = a // b
print(f"{a} // {b} = {hasil}")

# modulus
hasil = a % b
print(f"{a} % {b} = {hasil}")

# eksponen / pangkat
hasil = a ** b
print(f"{a} ** {b} = {hasil}")


